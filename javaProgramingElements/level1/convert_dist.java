package javaProgramingElements.level1;

/*
  convert km to miles (simple conversion).
  uses a basic multiplier like your version.
*/
public class convert_dist {
    public static void main(String[] args) {
        double km = 10.8;
        double miles = km * 0.6;
        System.out.println("the distance " + km + " km in miles is " + miles);
    }
}
